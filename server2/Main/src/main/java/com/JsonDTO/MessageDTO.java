package com.JsonDTO;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public  class MessageDTO {
    private int code;
    private String text;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }


}